class IntegrationReactSelect extends React.Component {
  state = {
    players: [],
    single: null,
    multi: null,
  };

  componentDidMount() {
    this.getPlayers();
    this.props.onChange(suggestions[0]);
  }

  getPlayers = _ => {
    fetch('http://localhost:4000/players')
    .then(response => response.json())
    .then(response => this.setState({ players: response.data}))
    .catch(err => console.error(err))
  }
  handleChange = name => value => {
    this.setState({
      [name]: value,
    });
    console.log(this.state.playerss.filter(word => word.label === value).length)
    if (this.state.playerss.filter(word => word.label === value.label).length === 1)
    {
      this.props.onChange(value);
    }
  };

  render() {
    const { classes, theme } = this.props;

    const selectStyles = {
      input: base => ({
        ...base,
        color: theme.palette.text.primary,
        '& input': {
          font: 'inherit',
        },
      }),
    };
    this.state.playerss = this.state.players.map(player => ({value: player.Name, label: player.Name}))
    console.log(this.state.playerss)
    return (
      <div className={classes.root}>
        <NoSsr>
          <Select
            classes={classes}
            styles={selectStyles}
            options={this.state.playerss}
            components={components}
            value={this.state.single}
            onChange={this.handleChange('single')}
            placeholder="Player"
          />
          <div className={classes.divider} />
        </NoSsr>
      </div>
    );
  }
}

IntegrationReactSelect.propTypes = {
  classes: PropTypes.object.isRequired,
  theme: PropTypes.object.isRequired,
};

export default withStyles(styles, { withTheme: true })(IntegrationReactSelect);